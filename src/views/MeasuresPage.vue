<div class="box">  In most of the world, recipes use the metric system of units—litres (L) and millilitres (mL), grams (g) and kilograms (kg), and degrees Celsius (°C). The official spelling litre is used in most English-speaking nations; the notable exception is the United States where the spelling liter is preferred.
The United States measures weight in pounds (avoirdupois), while recipes in the UK tend to include both imperial and metric measures, following the advice of the Guild of Food Writers.[5] The United States also uses volume measures based on cooking utensils and pre-metric measures. The actual values frequently deviate from the utensils on which they were based, and there is little consistency from one country to another.
